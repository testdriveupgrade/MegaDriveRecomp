std::string Lifter::read_operand(const m68k::Target& t, uint32_t sea, int bytes) const {
    const int bits = bytes * 8;
    const int idx  = t.index();
    switch (t.kind()) {
    case TK::DataRegisterKind:
        return fmt::format("ctx.d[{}]", idx);
    case TK::AddressRegisterKind:
        return fmt::format("ctx.a[{}]", idx);
    case TK::AddressKind:
    case TK::AddressIncrementKind:
        return fmt::format("recomp_mem_read{}(ctx.a[{}] & 0xFFFFFFU)", bits, idx);
    case TK::AddressDecrementKind:
        return fmt::format("recomp_mem_read{}(ctx.a[{}] & 0xFFFFFFU)", bits, idx);
    case TK::AddressDisplacementKind:
        return fmt::format("recomp_mem_read{}((ctx.a[{}] + (int32_t)(int16_t)0x{:04X}U) & 0xFFFFFFU)",
                           bits, idx, t.get_ext_word0());
    case TK::AddressIndexKind: {
        uint16_t ew = t.get_ext_word0();
        int xn = (ew >> 12) & 7;
        bool an = (ew >> 15) & 1;
        bool xl = (ew >> 11) & 1;
        int8_t d8 = (int8_t)(ew & 0xFF);
        std::string xr = an ? fmt::format("ctx.a[{}]", xn) : fmt::format("ctx.d[{}]", xn);
        std::string xcast = xl ? "(int32_t)" : "(int32_t)(int16_t)";
        return fmt::format("recomp_mem_read{}((ctx.a[{}] + {}({}) + (int32_t){})"
                           " & 0xFFFFFFU)", bits, idx, xcast, xr, (int)d8);
    }
    case TK::ProgramCounterDisplacementKind:
    case TK::AbsoluteShortKind:
    case TK::AbsoluteLongKind:
        return fmt::format("recomp_mem_read{}(0x{:06X}U)", bits, sea);
    case TK::ProgramCounterIndexKind: {
        // base is static, Xn is runtime
        uint16_t ew = t.get_ext_word0();
        int xn = (ew >> 12) & 7;
        bool an = (ew >> 15) & 1;
        bool xl = (ew >> 11) & 1;
        int8_t d8 = (int8_t)(ew & 0xFF);
        std::string xr = an ? fmt::format("ctx.a[{}]", xn) : fmt::format("ctx.d[{}]", xn);
        std::string xcast = xl ? "(int32_t)" : "(int32_t)(int16_t)";
        // sea holds pc_after - 2 ... but we reuse static_ea which was pc_after-2+disp
        // Actually sea = static_ea = 0 for ProgramCounterIndexKind (set in analyzer)
        // We need pc_after - 2 as the base; but we don't have it here.
        // Fall back: emit a TODO comment with runtime dispatch.
        return fmt::format("recomp_mem_read{}((0x{:06X}U + {}({}) + (int32_t){}) & 0xFFFFFFU)",
                           bits, sea, xcast, xr, (int)d8);
    }
    case TK::ImmediateKind:
        return fmt::format("0x{:08X}U", rom_read(t.get_address(), bytes));
    default:
        return "0 /* unknown operand */";
    }
}

std::string Lifter::write_operand(const m68k::Target& t, uint32_t sea, int bytes,
                                   const std::string& val) const {
    const int bits = bytes * 8;
    const int idx  = t.index();
    const uint32_t upper_mask = (bytes == 4) ? 0 : (~0U << (bytes * 8));
    const uint32_t lower_mask = (bytes == 4) ? ~0U : ((1U << (bytes * 8)) - 1);

    switch (t.kind()) {
    case TK::DataRegisterKind:
        if (bytes == 4)
            return fmt::format("ctx.d[{}] = {};", idx, val);
        return fmt::format("ctx.d[{0}] = (ctx.d[{0}] & 0x{1:08X}U) | ({2} & 0x{3:08X}U);",
                           idx, upper_mask, val, lower_mask);
    case TK::AddressRegisterKind:
        if (bytes == 4)
            return fmt::format("ctx.a[{}] = {};", idx, val);
        return fmt::format("ctx.a[{0}] = (ctx.a[{0}] & 0x{1:08X}U) | ({2} & 0x{3:08X}U);",
                           idx, upper_mask, val, lower_mask);
    case TK::AddressKind:
    case TK::AddressIncrementKind:
    case TK::AddressDecrementKind:
        return fmt::format("recomp_mem_write{}(ctx.a[{}] & 0xFFFFFFU, (uint{}_t)({}));",
                           bits, idx, bits, val);
    case TK::AddressDisplacementKind:
        return fmt::format("recomp_mem_write{}((ctx.a[{}] + (int32_t)(int16_t)0x{:04X}U) & 0xFFFFFFU, (uint{}_t)({}));",
                           bits, idx, t.get_ext_word0(), bits, val);
    case TK::AddressIndexKind: {
        uint16_t ew = t.get_ext_word0();
        int xn = (ew >> 12) & 7;
        bool an = (ew >> 15) & 1;
        bool xl = (ew >> 11) & 1;
        int8_t d8 = (int8_t)(ew & 0xFF);
        std::string xr = an ? fmt::format("ctx.a[{}]", xn) : fmt::format("ctx.d[{}]", xn);
        std::string xcast = xl ? "(int32_t)" : "(int32_t)(int16_t)";
        return fmt::format("recomp_mem_write{}((ctx.a[{}] + {}({}) + (int32_t){}) & 0xFFFFFFU, (uint{}_t)({}));",
                           bits, idx, xcast, xr, (int)d8, bits, val);
    }
    case TK::ProgramCounterDisplacementKind:
    case TK::AbsoluteShortKind:
    case TK::AbsoluteLongKind:
        return fmt::format("recomp_mem_write{}(0x{:06X}U, (uint{}_t)({}));",
                           bits, sea, bits, val);
    case TK::ProgramCounterIndexKind: {
        uint16_t ew = t.get_ext_word0();
        int xn = (ew >> 12) & 7;
        bool an = (ew >> 15) & 1;
        bool xl = (ew >> 11) & 1;
        int8_t d8 = (int8_t)(ew & 0xFF);
        std::string xr = an ? fmt::format("ctx.a[{}]", xn) : fmt::format("ctx.d[{}]", xn);
        std::string xcast = xl ? "(int32_t)" : "(int32_t)(int16_t)";
        return fmt::format("recomp_mem_write{}((0x{:06X}U + {}({}) + (int32_t){}) & 0xFFFFFFU, (uint{}_t)({}));",
                           bits, sea, xcast, xr, (int)d8, bits, val);
    }
    default:
        return fmt::format("/* unimplemented write {} */", val);
    }
}

std::string Lifter::pre_effect(const m68k::Target& t, int bytes) const {
    if (t.kind() != TK::AddressDecrementKind) return "";
    int idx = t.index();
    int adj = (idx == 7 && bytes == 1) ? 2 : bytes;
    return fmt::format("ctx.a[{}] -= {}; ", idx, adj);
}

std::string Lifter::post_effect(const m68k::Target& t, int bytes) const {
    if (t.kind() != TK::AddressIncrementKind) return "";
    int idx = t.index();
    int adj = (idx == 7 && bytes == 1) ? 2 : bytes;
    return fmt::format(" ctx.a[{}] += {};", idx, adj);
}

std::string Lifter::ea_expr(const m68k::Target& t, uint32_t sea, uint32_t pc_after) const {
    const int idx = t.index();
    switch (t.kind()) {
    case TK::AddressKind:
    case TK::AddressIncrementKind:
    case TK::AddressDecrementKind:
        return fmt::format("ctx.a[{}]", idx);
    case TK::AddressDisplacementKind:
        return fmt::format("(ctx.a[{}] + (int32_t)(int16_t)0x{:04X}U) & 0xFFFFFFU",
                           idx, t.get_ext_word0());
    case TK::AddressIndexKind: {
        uint16_t ew = t.get_ext_word0();
        int xn = (ew >> 12) & 7; bool an = (ew >> 15) & 1; bool xl = (ew >> 11) & 1;
        int8_t d8 = (int8_t)(ew & 0xFF);
        std::string xr = an ? fmt::format("ctx.a[{}]", xn) : fmt::format("ctx.d[{}]", xn);
        std::string xcast = xl ? "(int32_t)" : "(int32_t)(int16_t)";
        return fmt::format("(ctx.a[{}] + {}({}) + (int32_t){}) & 0xFFFFFFU",
                           idx, xcast, xr, (int)d8);
    }
    case TK::ProgramCounterDisplacementKind:
    case TK::AbsoluteShortKind:
    case TK::AbsoluteLongKind:
        return fmt::format("0x{:06X}U", sea);
    case TK::ProgramCounterIndexKind: {
        uint16_t ew = t.get_ext_word0();
        int xn = (ew >> 12) & 7; bool an = (ew >> 15) & 1; bool xl = (ew >> 11) & 1;
        int8_t d8 = (int8_t)(ew & 0xFF);
        uint32_t base = pc_after - 2;
        std::string xr = an ? fmt::format("ctx.a[{}]", xn) : fmt::format("ctx.d[{}]", xn);
        std::string xcast = xl ? "(int32_t)" : "(int32_t)(int16_t)";
        return fmt::format("(0x{:06X}U + {}({}) + (int32_t){}) & 0xFFFFFFU",
                           base, xcast, xr, (int)d8);
    }
    default:
        return "0 /* unknown EA */";
    }
}

uint32_t Lifter::static_ea(const m68k::Target& t, uint32_t pc_after) const {
    switch (t.kind()) {
    case TK::AbsoluteLongKind:
        return ((uint32_t)t.get_ext_word0() << 16) | t.get_ext_word1();
    case TK::AbsoluteShortKind:
        return (uint32_t)(int32_t)(int16_t)t.get_ext_word0();
    case TK::ProgramCounterDisplacementKind:
        return (uint32_t)((int32_t)pc_after - 2 + (int16_t)t.get_ext_word0());
    default:
        return 0;
    }
}

const char* Lifter::cond_expr(m68k::Instruction::Condition c) {
    switch (c) {
    case IC::TrueCond:           return "1";
    case IC::FalseCond:          return "0";
    case IC::HigherCond:         return "(!ctx.sr.c && !ctx.sr.z)";
    case IC::LowerOrSameCond:    return "(ctx.sr.c || ctx.sr.z)";
    case IC::CarryClearCond:     return "(!ctx.sr.c)";
    case IC::CarrySetCond:       return "(ctx.sr.c)";
    case IC::NotEqualCond:       return "(!ctx.sr.z)";
    case IC::EqualCond:          return "(ctx.sr.z)";
    case IC::OverflowClearCond:  return "(!ctx.sr.v)";
    case IC::OverflowSetCond:    return "(ctx.sr.v)";
    case IC::PlusCond:           return "(!ctx.sr.n)";
    case IC::MinusCond:          return "(ctx.sr.n)";
    case IC::GreaterOrEqualCond: return "(ctx.sr.n == ctx.sr.v)";
    case IC::LessThanCond:       return "(ctx.sr.n != ctx.sr.v)";
    case IC::GreaterThanCond:    return "(!ctx.sr.z && ctx.sr.n == ctx.sr.v)";
    case IC::LessOrEqualCond:    return "(ctx.sr.z || ctx.sr.n != ctx.sr.v)";
    default:                     return "0";
    }
}

uint32_t Lifter::rom_read(uint32_t addr, int bytes) const {
    uint32_t val = 0;
    for (int i = 0; i < bytes; ++i)
        val = (val << 8) | ((addr + i < rom_.size()) ? rom_[addr + i] : 0);
    return val;
}

std::string Lifter::areg(int n) {
    return fmt::format("ctx.a[{}]", n);
}
